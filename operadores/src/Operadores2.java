public class Operadores2 {
    public static void main(String[] args) {
        int numero = 5;
        // colocar o sinal de menos (-) antes da variável,
        // faz com que o retorno dela seja negativa, porém não é uma atribuição
        System.out.println(- numero);
            System.out.println(numero);

            //como fazer uma atribuição
            numero = - numero;
            System.out.println(numero);

            // para realizar a conversão de um negativo para positivo
            // é preciso multiplicar o numero por "-1"
            numero = numero* -1;
            System.out.println(numero);


    }
}
